// TODO: define polyfill for `Object.is(..)`
Object.is = (x ,y) => {
  let result = x === y;
  if (x === 0) result = (1/y === 1/x);
  result = Number.isNaN(x) && Number.isNaN(y) || result;

  return result;
};


